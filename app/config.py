import os
from dotenv import load_dotenv

# --- ОТЛАДКА: Проверка, что файл конфигурации импортируется ---
print("--- [DEBUG] Загрузка app/config.py началась ---")
load_dotenv()
# -----------------------------------------------------------

# ИСПРАВЛЕНО: Теперь ищем переменную с именем "SECRET_KEY",
# как указано в вашем .env файле.
SECRET_KEY = os.getenv("SECRET_KEY")
ALGORITHM = "HS256"

# Добавим проверку, чтобы убедиться, что ключ найден
if not SECRET_KEY:
    # Эту ошибку вы должны увидеть, если ключ не установлен в .env
    raise ValueError("Переменная окружения 'SECRET_KEY' не найдена. Проверьте ваш .env файл.")

# --- ОТЛАДКА: Проверка успешной загрузки ---
print(f"--- [DEBUG] SECRET_KEY успешно загружен (длина: {len(SECRET_KEY)}) ---")